export function AchievementNotification() {
  const { notifications, removeNotification } = useAchievementNotifications()
  
  useEffect(() => {
    if (notifications.length > 0) {
      const timer = setTimeout(() => {
        removeNotification(notifications[0].id)
      }, 4000)
      return () => clearTimeout(timer)
    }
  }, [notifications, removeNotification])
  
  return (
    <div className="fixed top-4 left-0 right-0 z-[100] flex flex-col items-center gap-2 pointer-events-none px-4">
      <AnimatePresence>
        {notifications.slice(0, 3).map((notification) => (
          <motion.div
            key={notification.id}
            initial={{ opacity: 0, y: -50, scale: 0.9 }}
            animate={{ opacity: 1, y: 0, scale: 1 }}
            exit={{ opacity: 0, y: -20, scale: 0.9 }}
            className="pointer-events-auto w-full max-w-sm"
          >
            <div className="bg-bg-surface border border-accent/30 rounded-xl p-4 shadow-lg shadow-accent/10">
              <div className="flex items-center gap-3">
                <div className={`w-12 h-12 rounded-xl flex items-center justify-center ${
                  notification.achievement.difficulty === 'hard'
                    ? 'bg-gradient-to-br from-warning-muted to-warning/10 border border-warning/30'
                    : notification.achievement.difficulty === 'medium'
                      ? 'bg-gradient-to-br from-accent/20 to-accent/10 border border-accent/30'
                      : 'bg-accent/10 border border-accent/20'
                }`}>
                  <AchievementIcon
                    icon={notification.achievement.icon}
                    className={`w-6 h-6 ${
                      notification.achievement.difficulty === 'hard'
                        ? 'text-warning'
                        : notification.achievement.difficulty === 'medium'
                          ? 'text-accent'
                          : 'text-accent/80'
                    }`}
                  />
                </div>
                <div className="flex-1 min-w-0">
                  <p className="text-[10px] uppercase tracking-wider text-accent font-medium">Achievement Unlocked</p>
                  <p className="text-sm font-semibold text-text truncate">{notification.achievement.name}</p>
                  <p className="text-xs text-text-muted truncate">{notification.achievement.description}</p>
                </div>
                <div className="text-right">
                  <span className="text-sm font-bold text-accent">+{notification.achievement.points}</span>
                </div>
              </div>
            </div>
          </motion.div>
        ))}
      </AnimatePresence>
    </div>
  )
}

export { AchievementIcon }
